int processing( int viewx, int viewy )
{

	int x, y, m, option, dx, dy, dm, greb;

	while(bioskey(1) == 0 & m_button()==0)
	{
		mutant ++;

		x = random(60);
		y = random(30);
		m = ( x * 30 ) + y;

		if( map[m].food < 0 & map[m].greble > 0 )
		{
			map[m].greble = 0;
			display_process( x, y, m, viewx, viewy );
		}

		if(map[m].food < 40)
		{
			map[m].food++;
		}
		else
		{
			display_process( x, y, m, viewx, viewy );
		}

		if( map[m].greble > 0 )
		{
			map[m].food = map[m].food - 3;

			option = random(4);
			switch(option)
			{
			case 0:
				dx = x+1;
				dy = y;
				break;

			case 1:
				dx = x-1;
				dy = y;
				break;

			case 2:
				dx = x;
				dy = y+1;
				break;

			case 3:
				dx = x;
				dy = y-1;
				break;
			}

			dm = ( dx * 30 ) + dy;
			greb = map[dm].greble;

			if( dx > 0 & dx < 60 & dy > 0 & dy < 30  )
			{

				switch(map[m].greble)
				{
				case 1:

					if(map[dm].tera==5 & greb==0 & map[dm].food>3 )
					{
							map[dm].greble = 1;
					}
					break;

				case 2:

					if( mutant > level + random(1000)  )
					{
						if( greb==0 & map[dm].food>3 & map[dm].tera!=0 & map[dm].tera!=16 )
						{
							map[dm].greble = 3;
							mutant = 0;
						}
					}
					else
					{
						if( map[dm].tera==0 & greb==0 & map[dm].food>3 )
						{
							map[dm].greble = 2;
						}
					}
					break;

				case 3:

					if( mutant > ( level + random(1000) ) )
					{
						if( greb==0 & map[dm].food>3 & map[dm].tera==5 )
						{
							map[dm].greble = 4;
							mutant = 0;
						}
					}
					else
					{

						if( greb==0 & map[dm].food>3 & map[dm].tera!=16 & map[dm].tera!=0 )
						{
							map[dm].greble = 3;
						}
					}
					break;

				case 4:

					if( mutant > ( level + random(1000) ) )
					{
						if( map[dm].food > 3 & map[dm].tera != 0 )
						{
							map[dm].greble = 5;
							if( map[dm].tera==16 ) map[dm].tera = 5;
							mutant = 0;
						}
					}
					else
					{
						if( map[dm].tera!=0 & greb==0 & map[dm].food>3 )
						{
							map[dm].greble = 4;
							if( map[dm].tera==16 ) map[dm].tera = 5;
						}
					}
					break;


				case 5:

					if( ( map[dm].food > 3 | (greb != 0 & greb != 5) ) & map[dm].tera == 5 )
					{
						if( greb != 0 & greb != 5)
						{
							map[m].food = 30;
							map[dm].food = 30;
						}

						map[dm].greble = 5;
						if( map[dm].tera==16 ) map[dm].tera = 5;

					}
					break;

				case 6:


					if( ( map[dm].tera == 5 | map[dm].tera == 16 ) & ( greb == 1 | greb == 3  | map[dm].food > 3 ) & greb != 5 )
					{
						if( greb == 1 | greb == 3 )
						{
							map[m].food = 30;
							map[dm].food = 30;
							if (map[dm].tera==5) map[dm].tera = 16;
							map[dm].greble = 6;
						}

						if( greb==0 ) map[dm].greble = 6;
					}
					break;

				};

				display_process( dx, dy, dm, viewx, viewy );
			}
		}
	}
}


